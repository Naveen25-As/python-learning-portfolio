# Count Digits, Alphabets, and Special Characters.
 

text = input("Enter a string: ")

digits = letters = special = 0

for char in text:
    
    if char.isdigit():
        digits += 1
    elif char.isalpha():
        letters += 1
    else:
        special += 1

print("Digits:", digits)
print("Alphabets:", letters)
print("Special Characters:", special)

































































































































































